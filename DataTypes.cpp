#include <iostream>
#include <string>

using namespace std;

int main(){
    // Integer data type
    int integerVar = 42.56;
    cout << "Integer: " << integerVar << endl;

    // Floating-point data type
    float floatVar = 3.142;
    cout << "Float: " << floatVar << endl;

    // Double data type
    double doubleVar = 2.718281828459045;
    cout << "Double: " << doubleVar << endl;

    // Character data type
    char charVar = 'A';
    cout << "Character: " << charVar << endl;

    // Boolean data type
    bool boolVar = true;
    cout << "Boolean: " << boolVar << endl;

    //String data type
    string stringVar = "Hello, World!";
    cout << "String: " << stringVar << endl;

    return 0;


}